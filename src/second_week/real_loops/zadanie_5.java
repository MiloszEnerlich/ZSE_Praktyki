package second_week.real_loops;

public class zadanie_5 {
    public static void main(String[] args) {

        int a=0;
        while (a<20){
            System.out.println(a);
            a+=2;
        }

        System.out.println("\n\n\n");

        int b=30;
        do{
            System.out.println(b);
            b-=3;
        }while(b>0);
    }
}